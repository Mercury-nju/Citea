export function getTranslation(lang: Language) {
  const langTranslations = translations[lang] || translations.en
  
  // Deep merge with English as fallback for missing keys
  const mergeWithFallback = (target: any, fallback: any): any => {
    if (typeof target !== 'object' || target === null) return target
    if (typeof fallback !== 'object' || fallback === null) return target
    
    const result: any = { ...target }
    for (const key in fallback) {
      if (!(key in result)) {
        result[key] = fallback[key]
      } else if (typeof result[key] === 'object' && typeof fallback[key] === 'object') {
        result[key] = mergeWithFallback(result[key], fallback[key])
      }
    }
    return result
  }
  
  return lang === 'en' ? langTranslations : mergeWithFallback(langTranslations, translations.en)
}

